            <h1>Le projet</h1>

            <p class='fancyP'>Ce projet à été réalisé dans le cadre du projet de deuxième année
                 du module <i>programmation web coté serveur</i> de l'IUT
                 informatique de Clermont-Ferrand. Le but était de développer un
                  site internet de gestion de tâches. L'utilisateur devait pouvoir
                  gérer des projets qui sont des groupes de tâches, ainsi que des
                   publiques, c'est à dire des tâches qui devaient être accessibles,
                   que l'utilisateur soit connecté ou non. Cette fonctionnalité
                   peut être intéressante dans le cas d'un intranet par exemple,
                   de façon à gérer des tâches communes à tous les utilisateurs.
            Néanmoins, nous avons ajouté quelques fonctionnalités, comme des degrés
            d'importances pour les tâches (faible, normal, important ou critique),
            ou la possibilité de créer un compte.</p>

